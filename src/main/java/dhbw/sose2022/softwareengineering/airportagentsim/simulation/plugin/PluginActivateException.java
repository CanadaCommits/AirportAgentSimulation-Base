package dhbw.sose2022.softwareengineering.airportagentsim.simulation.plugin;

public class PluginActivateException extends Exception {
	
	private static final long serialVersionUID = 7794929436196777853L;
	
	
	public PluginActivateException() {
		super();
	}
	
	public PluginActivateException(String message) {
		super(message);
	}
	
	public PluginActivateException(Throwable cause) {
		super(cause);
	}
	
	public PluginActivateException(String message, Throwable cause) {
		super(message, cause);
	}
	
}
